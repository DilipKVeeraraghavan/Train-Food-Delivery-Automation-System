    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try
        {
            if(acceptButton.isSelected())
            {
                Management.acceptRegistration(ma,fs.get(Integer.parseInt(jTextField1.getText())-1).getUsername());   
                refresh();
            }
            else if(rejectButton.isSelected())
            {
                Management.rejectRegistration(ma,fs.get(Integer.parseInt(jTextField1.getText())-1).getUsername());   
                refresh();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Please select an option");
            }
        } 
        catch (IllegalArgumentException ex) 
        {
            JOptionPane.showMessageDialog(null, "Invalid details");
        } 
        catch (UnableToConnectException ex) 
        {
            JOptionPane.showMessageDialog(null, "Unable to Connect!");
        } 
        catch (AuthenticationException ex) 
        {
            JOptionPane.showMessageDialog(null,ex.getMessage());
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, "Invalid sno");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


