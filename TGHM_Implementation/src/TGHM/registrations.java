    public registrations(ManagementAuth mauth) {
        initComponents();
        ma = mauth;
        try
        {
            fs = Management.getRegistrants(ma);
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel(); 
            for (int i = 0;i<fs.size();i++) {
                model.addRow(new Object[]{Integer.toString(i+1),fs.get(i).getName(),fs.get(i).getAuthCert().path,fs.get(i).getContact(),fs.get(i).getType()});
            }
            validate();
            repaint();
//            JOptionPane.showMessageDialog(null, "bf");
        } 
        catch (AuthenticationException ex) 
        {
            System.out.println("err:"+ex.getMessage());
        } 
        catch (UnableToConnectException ex) 
        {
            System.out.println("err:"+ex.getMessage());
        }
    }
    
