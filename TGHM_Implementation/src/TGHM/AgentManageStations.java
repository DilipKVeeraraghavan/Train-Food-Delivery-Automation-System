    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int err = 0;
        try
        {
            Integer.parseInt(codeField.getText());
        }
        catch(Exception e)
        {
            err =1;
            JOptionPane.showMessageDialog(null, "Invalid code");
        }
        if(search(Integer.parseInt(codeField.getText()))!=-1)
        {
            err = 1;
            JOptionPane.showMessageDialog(null, "Code Already Present");
        }
        if(err == 0)
        {
            try {
                stations.add(Allstations.get(searchAll(Integer.parseInt(codeField.getText()))));
                Management.setStations(stations, aa);
                refresh();
                JOptionPane.showMessageDialog(null, "Done");
            } catch (AuthenticationException | UnableToConnectException | IllegalArgumentException ex) {
                Logger.getLogger(ManageStations.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int err = 0;
        try
        {
            Integer.parseInt(snoField.getText());
        }
        catch(Exception e)
        {
            err =1;
            JOptionPane.showMessageDialog(null, "Invalid sno");
        }
        if(Integer.parseInt(snoField.getText())<1||Integer.parseInt(snoField.getText())>stations.size())
        {
            err =1;
            JOptionPane.showMessageDialog(null, "Invalid sno");
        }
        if(err == 0)
        {
            try
            {
                stations.remove(Integer.parseInt(snoField.getText())-1);
                Management.setStations(stations, aa);
                refresh();
                JOptionPane.showMessageDialog(null, "Done");
            } 
            catch (AuthenticationException | UnableToConnectException | IllegalArgumentException ex) 
            {
                Logger.getLogger(ManageStations.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed


