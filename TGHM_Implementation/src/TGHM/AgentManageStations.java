    private int search(int code)
    {
        for(int i=0;i<stations.size();i++)
        {
            if(stations.get(i).getCode().equals(Integer.toString(code)))
            {
                return i;
            }
        }
        return -1;
    }
    private void refresh()
    {
         try 
        {
            /*Stations list*/
            Allstations = Management.getAllStations(aa);
            DefaultListModel sourceModel = new DefaultListModel();
            for (int i = 0;i<Allstations.size();i++)
            {
                sourceModel.addElement(Allstations.get(i).getName()+"("+Allstations.get(i).getCode()+")");
            }
            source.setModel(sourceModel);
            
            /*Already present station*/
            stations = Management.getStations(aa);
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel(); 
            model.setRowCount(0);
            for (int i = 0;i<stations.size();i++) 
            {
                model.addRow(new Object[]{Integer.toString(i+1),stations.get(i).getName(),stations.get(i).getCode()});
            }
            validate();
            repaint();
        } catch (AuthenticationException | UnableToConnectException | IllegalArgumentException ex) 
        {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }
    private int searchAll(int code)
    {
        for(int i=0;i<Allstations.size();i++)
        {
            if(Allstations.get(i).getCode().equals(Integer.toString(code)))
            {
                return i;
            }
        }
        return -1;
    }
